package com.snycon.Beans;

public class Brand {

	private String name;
	private double max_prize ;
	private double min_prize;
	private int m_year;
	private String m_city ;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getMax_prize() {
		return max_prize;
	}
	public void setMax_prize(double max_prize) {
		this.max_prize = max_prize;
	}
	public double getMin_prize() {
		return min_prize;
	}
	public void setMin_prize(double min_prize) {
		this.min_prize = min_prize;
	}
	public int getM_year() {
		return m_year;
	}
	public void setM_year(int m_year) {
		this.m_year = m_year;
	}
	public String getM_city() {
		return m_city;
	}
	public void setM_city(String m_city) {
		this.m_city = m_city;
	}
	
	
}